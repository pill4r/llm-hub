export function t(key: string, vars?: Record<string, string | number>, lang?: Lang): string {
  const l = lang || (localStorage.getItem("llmhub_lang") as Lang) || "en";
  let str = dict[l]?.[key] || dict.en[key] || key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{${k}}`, String(v));
    }
  }
  return str;
}

export function getLang(): Lang {
  return (localStorage.getItem("llmhub_lang") as Lang) || "en";
}

export function setLang(lang: Lang) {
  localStorage.setItem("llmhub_lang", lang);
}
